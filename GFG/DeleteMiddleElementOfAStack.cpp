class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sizeOfStack)
    {
        // code here.. 
        stack<int> st;
        for(int i=sizeOfStack; i>ceil((sizeOfStack+1)/2); i--){
            int curr = s.top();
            s.pop();
            st.push(curr);
        }
        s.pop();
        for(int i=st.size(); i>0; i--){
            int curr = st.top();
            st.pop();
            s.push(curr);
        }
    }
};

